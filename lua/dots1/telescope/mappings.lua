local opts = {noremap = true, silent = true}
local keymap = vim.keymap.set

local builtin = require("telescope.builtin")
local file_browser = require "telescope".extensions.file_browser

keymap(
  "n",
  "<leader>ff",
  function()
    builtin.find_files()
  end,
  opts
)
keymap(
  "n",
  "<leader>fgf",
  function()
    builtin.git_files()
  end,
  opts
)
keymap(
  "n",
  "<leader>fw",
  function()
    builtin.live_grep()
  end,
  opts
)
keymap(
  "n",
  "<leader>bfw",
  function()
    builtin.current_buffer_fuzzy_find()
  end,
  opts
)
keymap(
  "n",
  "<leader>*",
  function()
    builtin.grep_string()
  end,
  opts
)
keymap(
  "n",
  "<leader>**",
  function()
    builtin.grep_string({grep_open_files = true})
  end,
  opts
)
keymap(
  "n",
  "<leader>lb",
  function()
    builtin.buffers()
  end,
  opts
)
keymap(
  "n",
  "<leader>lj",
  function()
    builtin.jumplist()
  end,
  opts
)
keymap(
  "n",
  "<leader>lpb",
  function()
    builtin.builtin()
  end,
  opts
)
keymap(
  "n",
  "<leader>lc",
  function()
    builtin.command_history()
  end,
  opts
)
keymap(
  "v",
  "<leader>lc",
  function()
    builtin.command_history()
  end,
  opts
)
keymap(
  "n",
  "<leader>ls",
  function()
    builtin.search_history()
  end,
  opts
)
keymap(
  "v",
  "<leader>ls",
  function()
    builtin.search_history()
  end,
  opts
)
keymap(
  "n",
  "<leader>lm",
  function()
    builtin.marks()
  end,
  opts
)
keymap(
  "n",
  "<leader>fnf",
  function()
    builtin.find_files {cwd = "~/.config/nvim"}
  end,
  opts
)
keymap(
  "n",
  "<leader>fb",
  function()
    file_browser.file_browser {hidden = true}
  end,
  opts
)
keymap(
  "n",
  "<leader>bl",
  function()
    builtin.buffers {show_all_buffers = true}
  end,
  opts
)
